document.addEventListener("DOMContentLoaded", function () {
    const header = document.querySelector("header");
    const toggleButton = document.getElementById("toggleHeader");
    const container = document.querySelector(".container");
    const icon = toggleButton.querySelector("i");

    // M·∫∑c ƒë·ªãnh ·∫©n header
    header.classList.remove("show");
    container.classList.remove("shift-down");
    icon.classList.remove("fa-x");
    icon.classList.add("fa-bars");

    toggleButton.addEventListener("click", function () {
        if (header.classList.contains("show")) {
            header.classList.remove("show");
            container.classList.remove("shift-down");
            icon.classList.remove("fa-x");
            icon.classList.add("fa-bars"); // ƒê·ªïi sang icon m·∫Øt b·ªã che
        } else {
            header.classList.add("show");
            container.classList.add("shift-down");
            icon.classList.remove("fa-bars");
            icon.classList.add("fa-x"); // ƒê·ªïi sang icon m·∫Øt m·ªü
        }
    });
});


